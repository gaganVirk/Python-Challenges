locations = ['delhi', 'auckland', 'wellington', 'invercargill']

# print list in original order
print(locations)

# use sorted() to print your list
print(sorted(locations))

# print the list in original order
print(locations)

# use reverse() to change the order
locations.reverse()
print(locations)

#use reverse again
locations.reverse()
print(locations)

# use sort to change your list alphabetically
locations.sort()
